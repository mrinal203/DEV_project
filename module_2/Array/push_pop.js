//  array push popo 
let arr = ["mrinal", "suraj", "raja"," mohanchandra"];
// for example if you want to add somthing new in array
// push -->add element in last
console.log(arr);
console.log("---------------------------");
arr.push(" ravi");
arr.push(89);
console.log(arr);
console.log("---------------------------");

// remove element from last 
arr.pop();
console.log(arr);
// add element from starting 
// use  unshift 

console.log("---------------------------");
arr.unshift(" ramesh");
console.log(arr);
// shift --> remove element from starting 

console.log("---------------------------");
arr.shift();
console.log(arr);
