// print all the days of week using switch case 

let day =0;
 switch(day){
  case 0:
     console.log("Sunday");
     break;
     case 1:
      console.log("monday");
      break;
      case 2:
     console.log("Tusday");
     break;
     case 3:
     console.log("Wednesday");
     break;
     case 4:
     console.log("Thrusday");
     break;
     case 5:
     console.log("Friday");
     break;
     case 6:
     console.log("saturday");
     break;
     default:
       console.log("INVALID DAY");
 }